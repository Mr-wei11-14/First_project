package com.demo;

public class Student {
    private String sno, name;
    public Student(){
    }

    public String getSno() {
        return sno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSno(String sno) {
        this.sno = sno;
    }
}
